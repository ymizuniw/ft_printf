#include "ft_printf.h"

t_bool	ft_is_spec(const char c)
{
	return (c == 'c' || c == 's' || c == 'd' || c == 'i' || c == 'u' || c == 'x'
		|| c == 'X' || c == 'p' || c == '%');
}
